#include <stdio.h>
// ^ This imports stdio header file
// stdio.h is a collection of standard input output function
// stdio stands for standard input output
// .h is header extension

int main() {
  // int here is the return type.
  // main() function is the function from where execution of any C program begins
  // That's why main() is mandatory for C program
  printf("Hello World\n");
  // printf is the acronym for print formatted
  // Function to print on screen.
  return 0;
  // return 0 in C Programming Language: return value of the main function is considered the "Exit Status" of the application.
}